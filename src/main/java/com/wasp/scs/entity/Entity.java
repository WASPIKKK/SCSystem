package com.wasp.scs.entity;

public abstract class Entity {

    private long id;
    private String name;

    protected Entity(String name) {
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
